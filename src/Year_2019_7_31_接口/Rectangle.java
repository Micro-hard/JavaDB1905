package Year_2019_7_31_接口;

public class Rectangle {//矩形
    private int length;
    private int width;

    public int getLength() {
        return length;
    }

    public int getWidth() {
        return width;
    }

    public Rectangle(int length, int width) {

        this.length = length;
        this.width = width;
    }
}
